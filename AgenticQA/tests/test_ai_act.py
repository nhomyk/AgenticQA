"""Unit tests for AIActComplianceChecker — @pytest.mark.unit"""
import pytest
from pathlib import Path

from agenticqa.compliance.ai_act import (
    AIActFinding,
    AIActComplianceChecker,
    ConformityEvidence,
)


def make_checker() -> AIActComplianceChecker:
    return AIActComplianceChecker()


def write(path: Path, content: str) -> Path:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")
    return path


