@pytest.mark.unit
class TestConformityScore:
    def test_all_present_score_one(self):
        checker = make_checker()
        findings = [
            AIActFinding("Art.9", "req", "present", "medium", "file.md", "tip"),
            AIActFinding("Art.13", "req", "present", "medium", "file.tsx", "tip"),
            AIActFinding("Art.14", "req", "present", "medium", "file.py", "tip"),
            AIActFinding("Art.22", "req", "present", "medium", "file.py", "tip"),
        ]
        assert checker._conformity_score(findings) == 1.0

    def test_all_missing_score_zero(self):
        checker = make_checker()
        findings = [
            AIActFinding("Art.9", "req", "missing", "critical", "not found", "tip"),
            AIActFinding("Art.13", "req", "missing", "high", "not found", "tip"),
        ]
        assert checker._conformity_score(findings) == 0.0

    def test_mixed_score(self):
        checker = make_checker()
        findings = [
            AIActFinding("Art.9", "req", "present", "medium", "f", "tip"),   # 1.0
            AIActFinding("Art.13", "req", "missing", "high", "f", "tip"),    # 0.0
        ]
        assert abs(checker._conformity_score(findings) - 0.5) < 0.01
